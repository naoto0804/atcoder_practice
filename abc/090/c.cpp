void Main() {
    // 自分を含めた近傍の個数nで決まる, nが偶数なら表, そうでないなら裏
    ll zero = 0;
    ll N, M; cin >> N >> M;
    if (N == 1 && M == 1){
        cout << 1 << endl;
    } else if (N == 1){
        cout << M - 2 << endl;
    } else if (M == 1){
        cout << N - 2 << endl;
    } else {
        cout << max(zero, N - 2) * max(zero, M - 2) << endl;
    }
}

