void Main() {
    ll N; cin >> N;
    map<int, int> a;
    rep(i, N){
        ll tmp; cin >> tmp;
        if (a.find(tmp) != a.end()){
            a[tmp] += 1;
        } else {
            a[tmp] = 1;
        }
    }
    ll cnt = 0;
    for (auto v: a){
        if (v.second > v.first){
            cnt += v.second - v.first;
        } else if (v.second < v.first){
            cnt += v.second;
        }
    }
    cout << cnt << endl;

}

