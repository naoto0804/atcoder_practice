void Main() {
    ll s; cin >> s;
    ll cnt = 1;
    ll prev = s;
    set<ll> x; x.insert(prev);
    while (true){
        if (prev % 2 == 0){
            prev = prev / 2;
        } else {
            prev = 3 * prev + 1;
        }
        cnt++;
        if (x.find(prev) != x.end()){break;}
        x.insert(prev);
    }
    cout << cnt << endl;
}

