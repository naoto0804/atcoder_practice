void Main() {
    ll X; cin >> X;
    // N 回目まで全部右移動すると 1+2+...+N=N(N+1)/2 動く
    // N(N+1)/2がXと等しいもしくは少しだけ大きくなるところまで行き，あとはどこを右移動しないか決める
    ll ans;
    ll x1 = floor(sqrt(2 * X));

    if (x1 * (x1 + 1) / 2 >= X){
        ans = x1;
    } else {
        ans = x1 + 1;
    }
    cout << ans << endl;
}

